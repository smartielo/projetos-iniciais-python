operacao = str(input("Qual operação deseja fazer? +, -, *, /: "))
numero1 = float(input("Digite o primeiro número: "))   
numero2 = float(input("Digite o segundo número: "))

if operacao == "+":(
    print(numero1 + numero2)
)
elif operacao == "-":(
    print(numero1 - numero2)
)
elif operacao == "*":(
    print(numero1 * numero2)
)
elif operacao == "/":(
    print(numero1 / numero2)
)